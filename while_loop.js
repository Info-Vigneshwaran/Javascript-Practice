/* While loop start */
console.log(`While loop start`);
let k = 3;
while (k <= 20) {
  console.log(`K= ${k}`);
  k++;
}
console.log(`While loop end`);
/* While loop end */
console.log(`\n`);

/* Do While loop start */
console.log(`DO While loop start`);
let q = 3;
do {
  console.log(`Q= ${q}`);
  q++;
} while (q <= 20);
console.log(`DO While loop end`);
/* Do While loop end */
console.log(`\n`);
